package com.mowitnow.model;

public class Pelouse {
    private int xmax;
    private int ymax;

    public Pelouse(int xmax, int ymax) {
        this.xmax = xmax;
        this.ymax = ymax;
    }

    public int getXmax() {
        return xmax;
    }

    public void setXmax(int xmax) {
        this.xmax = xmax;
    }

    public int getYmax() {
        return ymax;
    }

    public void setYmax(int ymax) {
        this.ymax = ymax;
    }
}
